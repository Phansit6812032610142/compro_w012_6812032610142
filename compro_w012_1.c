#include <stdio.h>

int main() {
    int x = 10;     // ประกาศตัวแปร x และกำหนดค่าเริ่มต้นให้เป็น 10
    int *ptr;       // ประกาศตัวชี้ (pointer) ที่จะเก็บที่อยู่ของตัวแปร x

    ptr = &x;       // ให้ pointer ptr ชี้ไปที่ตัวแปร x (เก็บ "address" ของ x)

    // แสดงค่าของ x ก่อนเปลี่ยนค่า
    printf("Before: x = %d\n", x);

    // ใช้ pointer เปลี่ยนค่าของ x เป็น 20
    *ptr = 20;      // *ptr หมายถึง "ค่าในช่องหน่วยความจำที่ ptr ชี้อยู่" (ก็คือค่า x)

    // แสดงค่าของ x หลังจากที่ pointer เปลี่ยนค่า
    printf("After: x = %d\n", x);

    return 0;   // จบโปรแกรม
}
